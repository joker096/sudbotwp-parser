/**
 * Утилита для транслитерации русского текста в латинский slug
 */

/**
 * Транслитерирует русский текст в URL-friendly slug
 * @param text - русский текст для транслитерации
 * @returns транслитерированный slug
 */
export function transliterate(text: string): string {
  if (!text) return '';

  const translitMap: Record<string, string> = {
    // Русские буквы -> латинские аналоги
    'а': 'a', 'б': 'b', 'в': 'v', 'г': 'g', 'д': 'd', 'е': 'e', 'ё': 'yo',
    'ж': 'zh', 'з': 'z', 'и': 'i', 'й': 'y', 'к': 'k', 'л': 'l', 'м': 'm',
    'н': 'n', 'о': 'o', 'п': 'p', 'р': 'r', 'с': 's', 'т': 't', 'у': 'u',
    'ф': 'f', 'х': 'h', 'ц': 'ts', 'ч': 'ch', 'ш': 'sh', 'щ': 'sch', 'ъ': '',
    'ы': 'y', 'ь': '', 'э': 'e', 'ю': 'yu', 'я': 'ya',
    // Заглавные буквы
    'А': 'A', 'Б': 'B', 'В': 'V', 'Г': 'G', 'Д': 'D', 'Е': 'E', 'Ё': 'Yo',
    'Ж': 'Zh', 'З': 'Z', 'И': 'I', 'Й': 'Y', 'К': 'K', 'Л': 'L', 'М': 'M',
    'Н': 'N', 'О': 'O', 'П': 'P', 'Р': 'R', 'С': 'S', 'Т': 'T', 'У': 'U',
    'Ф': 'F', 'Х': 'H', 'Ц': 'Ts', 'Ч': 'Ch', 'Ш': 'Sh', 'Щ': 'Sch', 'Ъ': '',
    'Ы': 'Y', 'Ь': '', 'Э': 'E', 'Ю': 'Yu', 'Я': 'Ya',
    // Пробелы и спецсимволы
    ' ': '-', '\t': '-', '\n': '-',
    // Удаляем нежелательные символы
    '!': '', '?': '', '.': '', ',': '', ';': '', ':': '',
    '"': '', "'": '', '(': '', ')': '', '[': '', ']': '',
    '{': '', '}': '', '<': '', '>': '', '/': '', '\\': '',
    '|': '', '@': '', '#': '', '$': '', '%': '', '^': '',
    '&': '', '*': '', '+': '=', '=': '-', '_': '-', '`': '',
    '~': '', '№': '',
  };

  // Применяем транслитерацию
  let result = text.split('').map(char => translitMap[char] || char).join('');

  // Удаляем повторяющиеся дефисы
  result = result.replace(/-{2,}/g, '-');

  // Удаляем дефисы в начале и конце
  result = result.replace(/^-+|-+$/g, '');

  // Заменяем любые не-ASCII символы на дефис
  result = result.replace(/[^\x00-\x7F]/g, '-');

  // Удаляем повторяющиеся дефисы после замены не-ASCII
  result = result.replace(/-{2,}/g, '-');
  result = result.replace(/^-+|-+$/g, '');

  return result.toLowerCase();
}

/**
 * Генерирует slug из заголовка
 * @param title - заголовок статьи
 * @returns готовый slug
 */
export function generateSlug(title: string): string {
  if (!title) return '';
  return transliterate(title);
}
